static struct snps_phy301_param_s *_snps_phy_301_get_configs(u32 pClk, dw_color_depth_t color,
		dw_pixel_repetition_t pixel)
{
	int i = 0;

	for (i = 0; phy301[i].clock != 0; i++) {

		pClk = _snps_phy_301_get_freq(pClk);

		if ((pClk == phy301[i].clock) &&
				(color == phy301[i].color) &&
				(pixel == phy301[i].pixel)) {
			return &(phy301[i]);
		}
	}
	hdmi_inf("EEROR: do not get phy_config\n");

	return NULL;
}

static int _snps_phy301_configure(void)
{
	u16 phyRead = 0;
	struct snps_phy301_param_s *config = NULL;
	struct dw_hdmi_dev_s *hdmi = dw_get_hdmi();

	u32 pixel_clk = hdmi->ctrl_dev.pixel_clk;
	dw_color_depth_t bits = hdmi->ctrl_dev.color_resolution;
	dw_pixel_repetition_t pixel = hdmi->ctrl_dev.pixel_repetition;

	log_trace();
	/* Color resolution 0 is 8 bit color depth */
	if (bits == 0)
		bits = DW_COLOR_DEPTH_8;

	config = _snps_phy_301_get_configs(pixel_clk, bits, pixel);
	if (config == NULL) {
		hdmi_err("synps phy301 table unsupport: clock %dhz, color depth %d, pixel repetition %d\n",
			pixel_clk, bits, pixel);
		return -1;
	}

	/* add calibrated resistor configuration for all video resolution */
	dw_write(0x10006, 0xc0);
	dw_write(0x10006, 0x80);

	dw_mc_reset_phy(1);

	dw_phy_power_enable(0);

	dw_phy_svsret();

	dw_mc_reset_phy(0);

	dw_phy_reconfigure_interface();

	dw_phy_write(OPMODE_PLLCFG, config->oppllcfg);
	if (dw_phy_read(OPMODE_PLLCFG, &phyRead) || (phyRead != config->oppllcfg))
		hdmi_err("OPMODE_PLLCFG Mismatch Write 0x%04x Read 0x%04x\n",
				config->oppllcfg, phyRead);

	dw_phy_write(PLLCURRCTRL, config->pllcurrctrl);
	if (dw_phy_read(PLLCURRCTRL, &phyRead) || (phyRead != config->pllcurrctrl))
		hdmi_err("PLLCURRCTRL Mismatch Write 0x%04x Read 0x%04x\n",
				config->pllcurrctrl, phyRead);

#if IS_ENABLED(CONFIG_ARCH_SUN50IW9)
	dw_phy_write(PLLGMPCTRL, 3);
	dw_phy_read(PLLGMPCTRL, &phyRead);
	if (phyRead != 3)
		hdmi_err("PLLGMPCTRL Mismatch Write 0x%04x Read 0x%04x\n",
				config->pllgmpctrl, phyRead);
#else
	dw_phy_write(PLLGMPCTRL, config->pllgmpctrl);
	dw_phy_read(PLLGMPCTRL, &phyRead);
	if (phyRead != config->pllgmpctrl)
		hdmi_err("PLLGMPCTRL Mismatch Write 0x%04x Read 0x%04x\n",
				config->pllgmpctrl, phyRead);
#endif

	dw_phy_write(TXTERM, config->txterm);
	if (dw_phy_read(TXTERM, &phyRead) || (phyRead != config->txterm))
		hdmi_err("TXTERM Mismatch Write 0x%04x Read 0x%04x\n",
				config->txterm, phyRead);

	dw_phy_write(CKSYMTXCTRL, config->cksymtxctrl);
	if (dw_phy_read(CKSYMTXCTRL, &phyRead) || (phyRead != config->cksymtxctrl))
		hdmi_err("CKSYMTXCTRL Mismatch Write 0x%04x Read 0x%04x\n",
				config->cksymtxctrl, phyRead);

	dw_phy_write(VLEVCTRL, config->vlevctrl);
	if (dw_phy_read(VLEVCTRL, &phyRead) || (phyRead != config->vlevctrl))
		hdmi_err("VLEVCTRL Mismatch Write 0x%04x Read 0x%04x\n",
				config->vlevctrl, phyRead);

	if (pixel_clk == 594000) {
		dw_phy_write(0x17, 0x8006);
		if (dw_phy_read(0x17, &phyRead) || (phyRead != 0x8006))
			hdmi_err("VLEVCTRL Mismatch Write 0x8006 Read 0x%04x\n",
							phyRead);
	}

	dw_phy_power_enable(1);

	if (dw_phy_wait_lock() == 1) {
		hdmi_inf("phy 301 pll locked!\n");
		return 0;
	}

	hdmi_err("phy 301 pll not locked!!!\n");
	return -1;
}

